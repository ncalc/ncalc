namespace NCalc.Parser;

/// <summary>
/// Defines the available argument separator options for function arguments.
/// </summary>
public enum ArgumentSeparator
{
    /// <summary>
    /// Use semicolon (;) as the argument separator.
    /// </summary>
    Semicolon,
    
    /// <summary>
    /// Use colon (:) as the argument separator.
    /// </summary>
    Colon,
    
    /// <summary>
    /// Use comma (,) as the argument separator.
    /// </summary>
    Comma
}
